import type { AppActionManifestEntry } from '@/lib/apps/manifest'

function sanitizeIdentifier(actionId: string): string {
  const cleaned = actionId.replace(/[^a-zA-Z0-9_]/g, '_')
  if (!cleaned || /^[0-9]/.test(cleaned)) return `action_${cleaned || 'unnamed'}`
  return cleaned
}

const APP_PUBLIC_FILE_TS =
  '{ url: string; name: string; mimeType: string; size: number }'

function isAppPublicFileSchema(schema: Record<string, unknown>): boolean {
  if (schema.type !== 'object') return false
  const props = schema.properties
  if (!props || typeof props !== 'object') return false
  const keys = Object.keys(props as Record<string, unknown>).sort()
  return (
    keys.join(',') === 'mimeType,name,size,url' &&
    Array.isArray(schema.required) &&
    schema.required.length === 4
  )
}

function jsonSchemaToTsType(schema: unknown, depth = 0): string {
  if (depth > 4 || schema == null || typeof schema !== 'object') return 'unknown'
  const s = schema as Record<string, unknown>
  const type = s.type

  if (type === 'string') return 'string'
  if (type === 'number' || type === 'integer') return 'number'
  if (type === 'boolean') return 'boolean'
  if (type === 'array') {
    if (s.items && typeof s.items === 'object' && isAppPublicFileSchema(s.items as Record<string, unknown>)) {
      return `Array<${APP_PUBLIC_FILE_TS}>`
    }
    const items = jsonSchemaToTsType(s.items, depth + 1)
    return `Array<${items}>`
  }
  if (isAppPublicFileSchema(s)) {
    return APP_PUBLIC_FILE_TS
  }
  if (type === 'object' || s.properties) {
    const props = (s.properties || {}) as Record<string, unknown>
    const required = new Set(
      Array.isArray(s.required) ? s.required.filter((r): r is string => typeof r === 'string') : []
    )
    const keys = Object.keys(props)
    if (keys.length === 0) {
      return s.additionalProperties === false ? 'Record<string, never>' : 'Record<string, unknown>'
    }
    const fields = keys.map((key) => {
      const optional = required.has(key) ? '' : '?'
      return `  ${JSON.stringify(key)}${optional}: ${jsonSchemaToTsType(props[key], depth + 1)}`
    })
    return `{\n${fields.join('\n')}\n}`
  }
  return 'unknown'
}

function outputTypeFromAllowlist(
  allowlist: AppActionManifestEntry['outputAllowlist']
): string {
  if (!allowlist.length) return 'Record<string, unknown>'
  const fields = allowlist.map((entry) => {
    const t = jsonSchemaToTsType(entry.schema)
    return `  ${JSON.stringify(entry.key)}: ${t === '{}' || t === 'unknown' ? 'unknown' : t}`
  })
  return `{\n${fields.join('\n')}\n}`
}

/**
 * Server-authored generated SDK from revision actions. Overwrites any user file.
 */
export function generateSimGeneratedTs(actions: AppActionManifestEntry[]): string {
  const lines: string[] = [
    '/** AUTO-GENERATED by Sim — do not edit. Regenerated on each app build. */',
    "import { createSimClientFromWindow } from '@sim/app-sdk'",
    '',
    'const sim = createSimClientFromWindow()',
    '',
  ]

  const used = new Set<string>()
  for (const action of actions) {
    let fnName = sanitizeIdentifier(action.actionId)
    if (used.has(fnName)) fnName = `${fnName}_${used.size}`
    used.add(fnName)

    const inputName = `${fnName[0].toUpperCase()}${fnName.slice(1)}Input`
    const outputName = `${fnName[0].toUpperCase()}${fnName.slice(1)}Output`

    lines.push(`export type ${inputName} = ${jsonSchemaToTsType(action.inputSchema)}`)
    lines.push(`export type ${outputName} = ${outputTypeFromAllowlist(action.outputAllowlist)}`)
    lines.push('')
    lines.push(
      `export async function ${fnName}(input: ${inputName}): Promise<${outputName}> {`,
      `  const result = await sim.run(${JSON.stringify(action.actionId)}, input as Record<string, unknown>)`,
      `  if (!result.success) {`,
      `    throw new Error(result.error || 'Action failed')`,
      `  }`,
      `  return result.outputs as ${outputName}`,
      `}`,
      ''
    )
  }

  return lines.join('\n')
}
